// ── Flat ticker array (used by all game logic) ────────────────────────────────
export const TICKERS = Object.keys(TICKER_INFO);

// ── Helper functions ──────────────────────────────────────────────────────────

/** Days since 2024-01-01 — matches the frontend seed formula exactly. */
export function getDailySeed() {
  const now   = new Date();
  const start = new Date(2024, 0, 1);
  return Math.floor((now - start) / (1000 * 60 * 60 * 24));
}

/** Today's answer ticker (deterministic, same for every user). */
export function getDailyTicker() {
  return TICKERS[getDailySeed() % TICKERS.length];
}

/** Random ticker for endless mode, excluding already-used ones. */
export function getRandomTicker(exclude = []) {
  const pool = TICKERS.filter(t => !exclude.includes(t));
  return pool[Math.floor(Math.random() * pool.length)];
}

/** Returns true if `ticker` is in the allowed list. */
export function isValidTicker(ticker) {
  return TICKERS.includes(ticker.toUpperCase().trim());
}
